#!/usr/bin/env node

/**
 * Validates and auto-fixes sidebars.ts against generated .api.mdx files.
 *
 * Usage:
 *   node scripts/sync-api-sidebar.mjs --check   CI gate: exits 1 if any .api.mdx
 *                                                file is missing from sidebars.ts
 *   node scripts/sync-api-sidebar.mjs --fix     Inserts missing entries into
 *                                                sidebars.ts using the frontmatter
 *                                                from each .api.mdx file
 *
 * Files excluded from scanning:
 *   - overview.mdx / *-overview.mdx  (category overview pages, manually managed)
 *   - *.info.mdx                     (API info pages generated by openapi-docs)
 *   - *.tag.mdx                      (tag pages generated by openapi-docs)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { parseArgs } from 'node:util';
import jscodeshift from 'jscodeshift';
import * as prettier from 'prettier';
import yaml from 'js-yaml';

const j = jscodeshift.withParser('tsx');

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const DOCS_ROOT = path.join(REPO_ROOT, 'docs');
const SIDEBARS_PATH = path.join(REPO_ROOT, 'sidebars.ts');

const API_DIRS = [
  path.join(DOCS_ROOT, 'api/client-api'),
  path.join(DOCS_ROOT, 'api/indexing-api'),
  path.join(DOCS_ROOT, 'api/platform-api'),
];

const INDEXING_SPEC = path.join(
  REPO_ROOT,
  'openapi/indexing/indexing-capitalized.yaml',
);
const PLATFORM_SPEC = path.join(
  REPO_ROOT,
  'openapi/platform/platform-capitalized.yaml',
);
const CLIENT_SPLIT_DIR = path.join(REPO_ROOT, 'openapi/client/split-apis');
const HTTP_METHODS = ['get', 'post', 'put', 'patch', 'delete'];

function shouldSkip(filename) {
  return (
    filename === 'overview.mdx' ||
    filename.endsWith('-overview.mdx') ||
    filename.endsWith('.info.mdx') ||
    filename.endsWith('.tag.mdx')
  );
}

function parseFrontmatterField(yaml, name) {
  const re = new RegExp(`^${name}:\\s*"?([^"\\n]+?)"?\\s*$`, 'm');
  const m = yaml.match(re);
  return m ? m[1] : null;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;
  const yaml = match[1];

  const sidebarLabel =
    parseFrontmatterField(yaml, 'sidebar_label') ??
    parseFrontmatterField(yaml, 'title');
  const sidebarClassName = parseFrontmatterField(yaml, 'sidebar_class_name');

  // "post api-method" → "api-method post" (sidebar uses base-class-first order)
  const className = sidebarClassName
    ? sidebarClassName.split(' ').reverse().join(' ')
    : null;

  return { label: sidebarLabel, className };
}

function collectApiDocs(dir) {
  const results = [];
  if (!fs.existsSync(dir)) return results;

  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      results.push(...collectApiDocs(path.join(dir, entry.name)));
    } else if (entry.name.endsWith('.api.mdx') && !shouldSkip(entry.name)) {
      const fullPath = path.join(dir, entry.name);
      const docId = path
        .relative(DOCS_ROOT, fullPath)
        .replace(/\.api\.mdx$/, '');
      const content = fs.readFileSync(fullPath, 'utf8');
      const frontmatter = parseFrontmatter(content);
      if (frontmatter) {
        results.push({ docId, filePath: fullPath, ...frontmatter });
      } else {
        console.warn(`Warning: could not parse frontmatter in ${fullPath}`);
      }
    }
  }
  return results;
}

function isOrphaned(docId) {
  const base = path.join(DOCS_ROOT, docId);
  return (
    !fs.existsSync(base + '.api.mdx') &&
    !fs.existsSync(base + '.mdx') &&
    !fs.existsSync(base + '-overview.mdx')
  );
}

function extractSidebarIds(root) {
  const ids = new Set();
  root
    .find(j.ObjectProperty, { key: { name: 'id' } })
    .filter((p) => {
      const v = p.node.value;
      return v.type === 'StringLiteral' && v.value.startsWith('api/');
    })
    .forEach((p) => ids.add(p.node.value.value));
  return ids;
}

function makeEntryNode(docId, label, className) {
  const props = [
    j.objectProperty(j.identifier('type'), j.stringLiteral('doc')),
    j.objectProperty(j.identifier('id'), j.stringLiteral(docId)),
    j.objectProperty(j.identifier('label'), j.stringLiteral(label)),
  ];
  if (className) {
    props.push(
      j.objectProperty(j.identifier('className'), j.stringLiteral(className)),
    );
  }
  return j.objectExpression(props);
}

function slugify(text) {
  return text
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

// Build a map: docId-slug -> tag-name from all OpenAPI source specs.
// Used to determine which sidebar category a flat-structured doc (e.g.
// indexing-api endpoints) belongs to, since the file path alone doesn't
// encode the category.
function buildSlugToTagMap() {
  const map = new Map();
  const specPaths = [];
  if (fs.existsSync(INDEXING_SPEC)) specPaths.push(INDEXING_SPEC);
  if (fs.existsSync(PLATFORM_SPEC)) specPaths.push(PLATFORM_SPEC);
  if (fs.existsSync(CLIENT_SPLIT_DIR)) {
    for (const f of fs.readdirSync(CLIENT_SPLIT_DIR)) {
      if (f.endsWith('.yaml')) specPaths.push(path.join(CLIENT_SPLIT_DIR, f));
    }
  }
  for (const specPath of specPaths) {
    let spec;
    try {
      spec = yaml.load(fs.readFileSync(specPath, 'utf8'));
    } catch (e) {
      console.warn(`Warning: failed to parse ${specPath}: ${e.message}`);
      continue;
    }
    for (const methods of Object.values(spec?.paths ?? {})) {
      if (!methods || typeof methods !== 'object') continue;
      for (const [m, op] of Object.entries(methods)) {
        if (!HTTP_METHODS.includes(m)) continue;
        if (!op || typeof op !== 'object') continue;
        const tag = op.tags?.[0];
        if (!op.summary || !tag) continue;
        map.set(slugify(op.summary), tag);
      }
    }
  }
  return map;
}

// Find the `items` array of the sidebar category whose label matches the
// given tag. Falls back to a `<tag> (...)` prefix match so a category can
// add a clarifying parenthetical (e.g. label "Custom Metadata (Any Datasource)"
// for OpenAPI tag "Custom Metadata") without breaking auto-insertion.
function findCategoryItemsByLabel(root, label) {
  let found = null;
  root.find(j.ObjectExpression).forEach((p) => {
    if (found) return;
    const props = p.node.properties;
    const isCategory = props.some(
      (pr) =>
        pr.type === 'ObjectProperty' &&
        pr.key?.name === 'type' &&
        pr.value?.type === 'StringLiteral' &&
        pr.value?.value === 'category',
    );
    if (!isCategory) return;
    const labelMatch = props.some(
      (pr) =>
        pr.type === 'ObjectProperty' &&
        pr.key?.name === 'label' &&
        pr.value?.type === 'StringLiteral' &&
        (pr.value.value === label || pr.value.value.startsWith(`${label} (`)),
    );
    if (!labelMatch) return;
    const itemsProp = props.find(
      (pr) => pr.type === 'ObjectProperty' && pr.key?.name === 'items',
    );
    if (itemsProp?.value?.type === 'ArrayExpression') {
      found = itemsProp.value;
    }
  });
  return found;
}

// Remove sidebar entries whose `id` is in `orphanedIds` (typically because the
// upstream OpenAPI spec renamed or removed the operation, leaving the entry
// pointing at a doc that no longer exists). Operates on every `items: [...]`
// array in the AST so nested categories are handled.
function removeOrphanEntries(root, orphanedIds) {
  if (orphanedIds.size === 0) return 0;
  let removed = 0;
  root.find(j.ObjectProperty, { key: { name: 'items' } }).forEach((p) => {
    const arr = p.node.value;
    if (arr.type !== 'ArrayExpression') return;
    const before = arr.elements.length;
    arr.elements = arr.elements.filter((el) => {
      if (!el || el.type !== 'ObjectExpression') return true;
      const idProp = el.properties.find(
        (prop) =>
          prop.type === 'ObjectProperty' &&
          prop.key?.name === 'id' &&
          prop.value?.type === 'StringLiteral',
      );
      if (!idProp) return true;
      return !orphanedIds.has(idProp.value.value);
    });
    removed += before - arr.elements.length;
  });
  return removed;
}

function insertEntry(root, doc, slugToTag) {
  const segments = doc.docId.split('/');
  const overviewId = segments.slice(0, -1).join('/') + '/overview';

  let inserted = false;

  root
    .find(j.ObjectProperty, { key: { name: 'items' } })
    .filter((p) => {
      const arr = p.node.value;
      if (arr.type !== 'ArrayExpression') return false;
      return arr.elements.some((el) => {
        if (!el || el.type !== 'ObjectExpression') return false;
        return el.properties.some(
          (prop) =>
            prop.type === 'ObjectProperty' &&
            prop.key?.name === 'id' &&
            prop.value?.type === 'StringLiteral' &&
            prop.value?.value === overviewId,
        );
      });
    })
    .forEach((p) => {
      if (inserted) return;
      p.node.value.elements.push(
        makeEntryNode(doc.docId, doc.label, doc.className),
      );
      inserted = true;
    });

  if (inserted) return true;

  // Fallback: look up the operation's OpenAPI tag and find the category whose
  // label matches. This handles flat-structure APIs (e.g. indexing-api) where
  // all docs live in one directory and the category is determined by the tag,
  // not the file path.
  const slug = segments[segments.length - 1];
  const tag = slugToTag.get(slug);
  if (tag) {
    const itemsArr = findCategoryItemsByLabel(root, tag);
    if (itemsArr) {
      itemsArr.elements.push(
        makeEntryNode(doc.docId, doc.label, doc.className),
      );
      return true;
    }
  }

  return false;
}

const { values: args } = parseArgs({
  options: {
    check: { type: 'boolean', default: false },
    fix: { type: 'boolean', default: false },
  },
});

if (!args.check && !args.fix) {
  console.error('Usage: node scripts/sync-api-sidebar.mjs --check | --fix');
  process.exit(1);
}

const allDocs = API_DIRS.flatMap(collectApiDocs);
const sidebarSource = fs.readFileSync(SIDEBARS_PATH, 'utf8');
const root = j(sidebarSource);
const existingIds = extractSidebarIds(root);
const missing = allDocs.filter((d) => !existingIds.has(d.docId));
const orphaned = [...existingIds].filter(isOrphaned);

if (args.check) {
  if (orphaned.length > 0) {
    console.warn(
      `Warning: ${orphaned.length} sidebar ID(s) have no corresponding .api.mdx file (investigate or clean up manually):`,
    );
    for (const id of orphaned) {
      console.warn(`  - ${id}`);
    }
    console.warn('');
  }

  if (missing.length === 0) {
    console.log('✓ sidebars.ts is complete — no missing API entries.');
    process.exit(0);
  }

  console.error(
    `✗ ${missing.length} .api.mdx file(s) are missing from sidebars.ts:\n`,
  );
  for (const doc of missing) {
    console.error(`  - ${doc.docId}`);
  }
  console.error(
    '\nRun `node scripts/sync-api-sidebar.mjs --fix` to auto-insert them.',
  );
  process.exit(1);
}

if (missing.length === 0 && orphaned.length === 0) {
  console.log('✓ sidebars.ts is already complete — nothing to do.');
  process.exit(0);
}

const warnings = [];
const slugToTag = buildSlugToTagMap();

for (const doc of missing) {
  const ok = insertEntry(root, doc, slugToTag);
  if (!ok) {
    const slug = doc.docId.split('/').slice(-1)[0];
    const tag = slugToTag.get(slug);
    const reason = tag
      ? `no sidebar category labelled '${tag}' (and no parent overview doc found)`
      : `no OpenAPI tag found for slug '${slug}' and no parent overview doc found`;
    warnings.push(`Could not auto-insert '${doc.docId}': ${reason}`);
  }
}

const orphanedSet = new Set(orphaned);
const removedCount = removeOrphanEntries(root, orphanedSet);

const transformed = root.toSource();
const prettierConfig = await prettier.resolveConfig(SIDEBARS_PATH);
const formatted = await prettier.format(transformed, {
  ...prettierConfig,
  filepath: SIDEBARS_PATH,
});
fs.writeFileSync(SIDEBARS_PATH, formatted, 'utf8');

if (missing.length > 0) {
  console.log(
    `✓ Inserted ${missing.length - warnings.length} entry(ies) into sidebars.ts:`,
  );
  for (const doc of missing) {
    console.log(`  + ${doc.docId}  "${doc.label}"`);
  }
}

if (removedCount > 0) {
  console.log(
    `\n✓ Removed ${removedCount} orphan entry(ies) from sidebars.ts (no corresponding .api.mdx / .mdx file):`,
  );
  for (const id of orphaned) {
    console.log(`  - ${id}`);
  }
}

if (warnings.length > 0) {
  console.warn(
    '\n⚠ The following entries could not be auto-inserted (new category?):',
  );
  for (const w of warnings) {
    console.warn(`  ! ${w}`);
  }
  console.warn(
    '\nAdd them manually to sidebars.ts. Continuing with partial fix so the regenerate workflow can still produce a PR for the entries that were auto-inserted.',
  );
}
